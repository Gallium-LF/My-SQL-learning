--无关子查询
--查询与“RAUL”在同一个系学习的同学的学号、姓名、所在系
USE JXGL
GO
	SELECT SDPET
	FROM S$
	WHERE SNAME='RAUL'
GO

USE JXGL
GO
	SELECT SNO,SNAME,SDPET
	FROM S$
	WHERE SDPET='MT'
GO
--整合
USE JXGL
GO
	SELECT SNO,SNAME,SDPET
	FROM S$
	WHERE SDPET IN
		(SELECT SDPET
		FROM S$
		WHERE SNAME='RAUL');
GO
--连接实现
USE JXGL
GO
	SELECT S1.SNO,S1.SNAME,S1.SDPET
	FROM S$ S1,S$ S2
	WHERE S1.SDPET=S2.SDPET AND S2.SNAME='RAUL';
GO

--查询其他系中比cs系某个同学年龄小的同学
--基于ANY
USE JXGL
GO
	SELECT SNO,AGE
	FROM S$
	WHERE AGE <ANY
		(SELECT AGE
		FROM S$
		WHERE SDPET!='CS')
	AND SDPET<>'CS';
GO
--基于MAX
USE JXGL
GO
	SELECT SNO,AGE
	FROM S$
	WHERE AGE <
		(SELECT MAX(AGE)
		FROM S$
		WHERE SDPET!='CS')
	AND SDPET<>'CS';
GO
